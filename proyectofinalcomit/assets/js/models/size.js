class Size {
    constructor(altura, ancho, profundidad) {
        this.altura = altura;
        this.ancho = ancho;
        this.profundidad = profundidad;
    }

    getAltura() {
        return this.altura;
    }

    setAltura(altura) {
        this.altura = altura;
    }

    getAncho() {
        return this.ancho;
    }

    setAncho(ancho) {
        this.ancho = ancho;
    }

    getProfundidad() {
        return this.profundidad;
    }

    setProfundidad(profundidad) {
        this.profundidad = profundidad;
    }
}